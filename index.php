                    <h2 id="biografi">Biografi</h2>
                    <p>Dewa pertama kali dibentuk oleh empat orang siswa SMP Negeri 6 Surabaya. Pada tahun 1985 band ini awalnya bernama "Booster". Kemudian setahun berselang pada 26 Agustus 1986, nama Dewa tercetus. Nama Dewa merupakan akronim mereka berempat: Dhani Ahmad (kibor, vokal), Erwin Prasetya (bas), Wawan Juniarso (drum), dan Andra Junaidi (gitar) mereka memiliki markas tempat berlatih di rumah Wawan di Jalan Dharmawangsa Dalam Selatan No. 7, yang terletak di komplek Universitas Airlangga.</p>
                    <p>Dewa yang awalnya muncul dengan musik yang lebih pop kemudian berubah haluan menjadi jaz setelah Erwin memperkenalkan musik jaz ke dalam grup ini. Wawan yang merupakan penggemar berat musik rok kemudian memutuskan keluar pada tahun 1987 dan bergabung dengan Outsider yang antara lain beranggotakan Ari Lasso dan Piyu. Posisi Wawan kemudian digantikan oleh Ari Sudono, Rizky Noviar, hingga yang terakhir Salman Harroen dan nama Dewa pun diubah menjadi Down Beat, yang diambil dari nama majalah jaz terbitan Amerika Serikat. Di kawasan Jawa Timur dan sekitarnya, nama Down Beat waktu itu cukup terkenal, terutama setelah berhasil merajai panggung festival. Sebut saja Festival Jazz Remaja se-Jawa Timur, juara I Festival band SLTA '90 atau juara II Jarum Super Fiesta Musik.</p>
                    <p>Ketika nama Slank berkibar, Wawan kembali dipanggil untuk menghidupkan Dewa dengan mengajak pula Ari Lasso. Nama Down Beat pun kembali berubah menjadi Dewa. Kali ini, Dewa hadir dengan mencampuradukkan beragam musik jadi satu: pop, rok, bahkan jaz, sehingga melahirkan alternatif baru bagi khazanah musik Indonesia saat itu.</p>
                    <p>Salah seorang teman sekelas Wawan, Harun ternyata tertarik pada konsep tersebut dan menawarkan investasi sebesar Rp10 juta untuk memodali teman-temannya membuat master rekaman. Karena di Surabaya tidak ada studio yang memenuhi syarat, mereka terpaksa berhijrah ke Jakarta meskipun dengan modal yang pas-pasan.</p>
